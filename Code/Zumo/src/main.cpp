void displayBitmap(const uint8_t *bitmap) 
{
    for (uint16_t i = 0; i < 1024; i++) 
        display.write(pgm_read_byte(bitmap + i));
    display.display();
}

int16_t mapSpeed(uint8_t val, bool formJoystick)
{
    if(formJoystick)
    {
        if (val > ZERO) return (int16_t)ceil(FACTOR_JOYSTICK * (val - ZERO + 1) + 1) * -1;
        else if (val < ZERO) return (int16_t)floor(FACTOR_JOYSTICK * (val - ZERO - 1) - 1) * -1;
        else return 0;
    }
    else
    {
        if (val > ZERO) return (int16_t)ceil(FACTOR * (val - ZERO + 1) + 1);
        else if (val < ZERO) return (int16_t)floor(FACTOR * (val - ZERO - 1) - 1);
        else return 0;
    }
}

void steerMotors(uint8_t left, uint8_t right, bool formJoystick = false)
{
    motors.setRightSpeed(mapSpeed(right,formJoystick));
    motors.setLeftSpeed(mapSpeed(left, formJoystick));
}

