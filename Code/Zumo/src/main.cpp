void loop() {
    #ifdef A_STAR
    if (mySerial.available() != 0) {
        String dataStr = mySerial.readStringUntil(';');
        dataStr.trim();
        //Read data
        uint8_t commaCount = 0;
        uint8_t lastComma = 0;
        uint8_t data[8] = {};
        for (uint8_t i = 0; i < dataStr.length(); i++)
            if (dataStr[i] == ',') 
            {
                commaCount++;
                if (commaCount > 7) return;
                data[commaCount - 1] = dataStr.substring(lastComma, i).toInt();
                lastComma = i + 1;
            }
        data[7] = dataStr.substring(lastComma, dataStr.length()).toInt();
        
        /*Serial.println(data[2]);
        Serial.println(data[3]);
        Serial.println(data[0]);
        Serial.println(data[1]);
        Serial.println(data[4]);
        Serial.println(data[6]);*/

        //SterMotors
        if(data[2] == 255) steerMotors(255, 0);//Right
        else if(data[3] == 255) steerMotors(0, 255); //Left 
        else if(data[0] == 255) steerMotors(255,255); //Up
        else if(data[1] == 255) steerMotors(0,0); //Down
        else steerMotors(data[4], data[6], true); //Joystick
        time = millis();
    }
    else if (time + 250 < millis()) steerMotors(ZERO,ZERO);
    #else
    if(type == 1 && error == 0)
    {
        ps2x.read_gamepad();

        Serial.print(ps2x.Analog(PSAB_PAD_UP), DEC);
        Serial.print(",");
        Serial.print(ps2x.Analog(PSAB_PAD_DOWN), DEC);
        Serial.print(",");
        Serial.print(ps2x.Analog(PSAB_PAD_RIGHT), DEC);
        Serial.print(",");
        Serial.print(ps2x.Analog(PSAB_PAD_LEFT), DEC);
        Serial.print(",");
        Serial.print(ps2x.Analog(PSS_LY), DEC);  
        Serial.print(",");
        Serial.print(ps2x.Analog(PSS_LX), DEC); 
        Serial.print(",");
        Serial.print(ps2x.Analog(PSS_RY), DEC); 
        Serial.print(",");
        Serial.print(ps2x.Analog(PSS_RX), DEC); 
        Serial.println(";");
    }
    #endif
}